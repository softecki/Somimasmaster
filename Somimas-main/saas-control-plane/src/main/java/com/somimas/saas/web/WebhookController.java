package com.somimas.saas.web;

import com.somimas.saas.payment.WebhookService;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestHeader;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/webhooks/flutterwave")
public class WebhookController {

    private final WebhookService webhookService;

    public WebhookController(WebhookService webhookService) {
        this.webhookService = webhookService;
    }

    @PostMapping
    public ResponseEntity<Void> handleFlutterwaveWebhook(@RequestHeader("verif-hash") String verifHash,
            @RequestBody String payload) {
        webhookService.handleFlutterwaveWebhook(verifHash, payload);
        return ResponseEntity.ok().build();
    }
}
